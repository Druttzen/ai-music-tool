#!/usr/bin/env node
/**
 * Generates docs/panel-inventory.md from GUIDED_PANEL_IDS and component wiring.
 * Run: npm run docs:panels
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const stepFocusPath = path.join(root, "app/lib/suno-guided-step-focus.js");
const componentsDir = path.join(root, "app/components");

const PANEL_COMPONENT_MAP = {
  guidedPath: "center-guided-path-panel.jsx → CenterGuidedPathPanel",
  maestro: "center-maestro-chat-panel.jsx → CenterMaestroChatPanel",
  idea: "center-idea-panel.jsx → CenterIdeaPanel",
  lyricStyle: "center-lyric-style-panel.jsx → CenterLyricStylePanel",
  voiceStyle: "center-voice-style-panel.jsx → CenterVoiceStylePanel",
  voiceCharacter: "center-voice-character-studio.jsx → CenterVoiceCharacterStudio",
  vocalEmbed: "center-vocal-embed-studio.jsx → CenterVocalEmbedStudio",
  analyzers: "center-analyzers-panel.jsx → CenterAnalyzersPanel",
  styleDna: "center-style-dna-search-panel.jsx → CenterStyleDnaSearchPanel",
  mood: "center-mood-panel.jsx → CenterMoodPanel",
  musicControls: "center-music-controls-panel.jsx → CenterMusicControlsPanel",
  coProducerQuick: "center-co-producer-panel.jsx → CenterCoProducerQuickPanel",
  coProducer: "center-co-producer-panel.jsx → CenterCoProducerPanel",
  sunoReimport: "center-suno-reimport-panel.jsx → CenterSunoReimportPanel",
  variations: "center-variations-panel.jsx → CenterVariationsPanel",
  proMode: "center-pro-mode-panel.jsx → CenterProModePanel",
  stylePresets: "page-sidebar-left.jsx (inline Style Presets)",
  saveLoad: "page-sidebar-left.jsx (inline Save / Load / Reset)",
  mode: "page-sidebar-left.jsx (inline Mode + intensity)",
  proModeLeft: "page-sidebar-left.jsx (inline Pro mode toggles)",
  promptPreview: "page-sidebar-right.jsx (inline Prompt Preview)",
  sunoValidator: "page-sidebar-right.jsx (inline Suno validator)",
  sunoLanguageIndex: "suno-language-index-panel.jsx → SunoLanguageIndexPanel (lazy)",
  history: "page-sidebar-right.jsx (inline History)",
  trackScoring: "page-sidebar-right.jsx (inline Track scoring)",
};

function readGuidedPanelIds() {
  const src = fs.readFileSync(stepFocusPath, "utf8");
  const block = src.match(/export const GUIDED_PANEL_IDS = \{([\s\S]*?)\};/);
  if (!block) throw new Error("GUIDED_PANEL_IDS not found");
  const ids = {};
  for (const line of block[1].split("\n")) {
    const m = line.match(/^\s*(\w+):\s*"(\w+)"/);
    if (m) ids[m[1]] = m[2];
  }
  return ids;
}

function readStepPanels() {
  const src = fs.readFileSync(stepFocusPath, "utf8");
  const steps = {};
  const stepRe = /^\s*(\d+):\s*\{/gm;
  let match;
  while ((match = stepRe.exec(src)) !== null) {
    const step = Number(match[1]);
    const start = match.index;
    const slice = src.slice(start);
    const center = [...slice.matchAll(/GUIDED_PANEL_IDS\.(\w+)/g)]
      .filter((_, i, arr) => {
        const before = slice.slice(0, arr[i].index);
        return before.includes("center:") && !before.includes("left:") && !before.includes("right:");
      })
      .map((m) => m[1]);
    const leftBlock = slice.match(/left:\s*\[([\s\S]*?)\]/);
    const centerBlock = slice.match(/center:\s*\[([\s\S]*?)\]/);
    const rightBlock = slice.match(/right:\s*\[([\s\S]*?)\]/);
    const pick = (block) =>
      block
        ? [...block[1].matchAll(/GUIDED_PANEL_IDS\.(\w+)/g)].map((m) => m[1])
        : [];
    steps[step] = {
      center: pick(centerBlock),
      left: pick(leftBlock),
      right: pick(rightBlock),
    };
  }
  return steps;
}

function verifyComponentsExist() {
  const missing = [];
  for (const entry of Object.values(PANEL_COMPONENT_MAP)) {
    const file = entry.split(" ")[0];
    if (file.endsWith(".jsx") && !fs.existsSync(path.join(componentsDir, file))) {
      missing.push(file);
    }
  }
  return missing;
}

const ids = readGuidedPanelIds();
const steps = readStepPanels();
const missing = verifyComponentsExist();
const generatedAt = new Date().toISOString().slice(0, 10);

let md = `# Panel inventory\n\n`;
md += `Auto-generated by \`npm run docs:panels\`. Last run: **${generatedAt}**.\n\n`;
md += `Maps \`GUIDED_PANEL_IDS\` → UI components and guided-step visibility.\n\n`;

md += `## All panel IDs\n\n`;
md += `| ID key | panelId | Component |\n|--------|---------|----------|\n`;
for (const [key, panelId] of Object.entries(ids)) {
  const component = PANEL_COMPONENT_MAP[key] || "_(unmapped)_";
  md += `| \`${key}\` | \`${panelId}\` | ${component} |\n`;
}

md += `\n## Guided step visibility\n\n`;
for (const [step, cols] of Object.entries(steps)) {
  md += `### Step ${step}\n\n`;
  md += `- **Center:** ${cols.center.map((k) => `\`${k}\``).join(", ") || "—"}\n`;
  md += `- **Left:** ${cols.left.map((k) => `\`${k}\``).join(", ") || "—"}\n`;
  md += `- **Right:** ${cols.right.map((k) => `\`${k}\``).join(", ") || "—"}\n\n`;
}

if (missing.length) {
  md += `## Warnings\n\n`;
  md += missing.map((f) => `- Missing component file: \`${f}\``).join("\n");
  md += `\n`;
}

const outPath = path.join(root, "docs/panel-inventory.md");
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, md);
console.log(`docs:panels — wrote ${outPath} (${Object.keys(ids).length} panels, ${Object.keys(steps).length} steps)`);
