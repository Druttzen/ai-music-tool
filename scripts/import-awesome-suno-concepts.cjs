#!/usr/bin/env node
/**
 * Import curated CC0 Suno style concepts from awesome-suno-prompts.
 *
 * Source: https://github.com/naqashmunir21/awesome-suno-prompts (CC0-1.0)
 * Re-run: npm run import:awesome-suno
 */

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const OUT_JS = path.join(ROOT, "app", "lib", "awesome-suno-concepts-synced.js");
const EXTENSIONS_JS = path.join(ROOT, "app", "lib", "style-catalog-extensions.js");

const SOURCE = {
  repo: "https://github.com/naqashmunir21/awesome-suno-prompts",
  license: "CC0-1.0",
  files: [
    "prompts/pop.md",
    "prompts/rock.md",
    "prompts/edm.md",
    "prompts/hip-hop.md",
    "prompts/indie.md",
    "prompts/jazz-blues.md",
    "prompts/country.md",
    "prompts/rnb-soul.md",
    "examples/advanced-techniques.md",
    "examples/before-after.md",
    "examples/common-mistakes.md",
    "examples/viral-hits.md",
    "PROJECT_CONTEXT.md",
    "PROMOTION_TEMPLATES.md",
    "CONTRIBUTING.md",
  ],
};

const BLOCKED = /\b(taylor swift|drake|the weeknd|beyonc|kanye|billie eilish|ariana grande|ed sheeran)\b/i;

function isEnglishLine(line) {
  return line && !/[\u0600-\u06FF\u3040-\u30FF\u4E00-\u9FFF\uAC00-\uD7AF]/u.test(line);
}

function cleanPromptBlock(block) {
  return block
    .split("\n")
    .map((line) => line.trim())
    .filter((line) => line && !/^BPM:/i.test(line) && !/^Key:/i.test(line))
    .join(", ")
    .replace(/\s+/g, " ")
    .trim();
}

function extractPromptBlocks(markdown) {
  const blocks = [];
  const re = /```([\s\S]*?)```/g;
  let match;
  while ((match = re.exec(markdown)) !== null) {
    const cleaned = cleanPromptBlock(match[1]);
    if (cleaned.length < 24 || cleaned.length > 280) continue;
    if (BLOCKED.test(cleaned)) continue;
    if (!isEnglishLine(cleaned)) continue;
    blocks.push(cleaned);
  }
  return blocks;
}

function readExportedStringArray(filePath, exportName) {
  const src = fs.readFileSync(filePath, "utf8");
  const re = new RegExp(`export const ${exportName} = \\[([\\s\\S]*?)\\];`, "m");
  const match = src.match(re);
  if (!match) return [];
  const lines = [];
  for (const m of match[1].matchAll(/"([^"\\]*(?:\\.[^"\\]*)*)"/g)) {
    const text = m[1].replace(/\\"/g, '"').trim();
    if (text.length >= 24 && text.length <= 280 && isEnglishLine(text) && !BLOCKED.test(text)) {
      lines.push(text);
    }
  }
  return lines;
}

async function fetchText(url) {
  const res = await fetch(url, { headers: { "User-Agent": "ai-music-tool-awesome-suno/1.0" } });
  if (!res.ok) throw new Error(`Fetch failed ${url}: ${res.status}`);
  return res.text();
}

function inferConceptTag(line) {
  const head = String(line).split(",")[0].trim().toLowerCase();
  const token = head.split(/\s+/)[0];
  return token || "other";
}

function emitJs(payload) {
  return `/**
 * AUTO-GENERATED by scripts/import-awesome-suno-concepts.cjs — do not edit manually.
 * Re-run: npm run import:awesome-suno
 *
 * Source: ${SOURCE.repo} (${SOURCE.license})
 * Synced at: ${payload.syncedAt}
 * Concept count: ${payload.lines.length}
 */

export const AWESOME_SUNO_CONCEPTS_SYNC = ${JSON.stringify(payload, null, 2)};

export const awesomeSunoConceptLines = AWESOME_SUNO_CONCEPTS_SYNC.lines;

export const awesomeSunoConceptTags = AWESOME_SUNO_CONCEPTS_SYNC.tags;
`;
}

async function main() {
  const seen = new Set();
  const lines = [];

  const addLine = (block, origin) => {
    const key = block.toLowerCase();
    if (seen.has(key)) return;
    seen.add(key);
    lines.push(block);
    if (origin) {
      // tracked in payload supplemental only
    }
  };

  for (const file of SOURCE.files) {
    const url = `https://raw.githubusercontent.com/naqashmunir21/awesome-suno-prompts/main/${file}`;
    console.log(`Fetching ${file} …`);
    const markdown = await fetchText(url);
    for (const block of extractPromptBlocks(markdown)) {
      addLine(block, file);
    }
  }

  const supplemental = {
    eraAnchoredGenres: readExportedStringArray(EXTENSIONS_JS, "eraAnchoredGenres"),
    trendMicroGenres2026: readExportedStringArray(EXTENSIONS_JS, "trendMicroGenres2026"),
    communityStyleSeeds: readExportedStringArray(EXTENSIONS_JS, "communityStyleSeeds"),
  };
  for (const block of [
    ...supplemental.eraAnchoredGenres,
    ...supplemental.trendMicroGenres2026,
    ...supplemental.communityStyleSeeds,
  ]) {
    addLine(block, "style-catalog-extensions");
  }

  lines.sort((a, b) => a.localeCompare(b));
  const capped = lines.slice(0, 400);
  const tags = Object.fromEntries(capped.map((line) => [line, inferConceptTag(line)]));

  const payload = {
    syncedAt: new Date().toISOString(),
    source: SOURCE,
    supplemental,
    lines: capped,
    tags,
  };

  fs.writeFileSync(OUT_JS, emitJs(payload), "utf8");
  console.log(
    `Wrote ${OUT_JS} (${capped.length} concepts — CC0 blocks + ${supplemental.eraAnchoredGenres.length} era + ${supplemental.trendMicroGenres2026.length} trend + ${supplemental.communityStyleSeeds.length} community seeds)`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
