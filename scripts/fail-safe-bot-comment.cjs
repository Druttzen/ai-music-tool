#!/usr/bin/env node
/**
 * Format a PR comment body from CI failure log text (stdin or file path arg).
 */
const fs = require("fs");
const { importFailSafeBot } = require("./fail-safe-bot-import.cjs");

async function main() {
  const arg = process.argv[2];
  const log = arg && arg !== "-"
    ? fs.readFileSync(arg, "utf8")
    : fs.readFileSync(0, "utf8");

  const { classifyFailureText, formatReportSummary, formatAgentFixPrompt, getActionableIssues } =
    await importFailSafeBot();

  const issues = classifyFailureText(log);
  const report = {
    at: Date.now(),
    overall: issues.length ? "fail" : "warn",
    issues: issues.length
      ? issues
      : [
          {
            id: "unclassified",
            severity: "warn",
            title: "Unclassified CI failure",
            detail: "See failed job log. Run npm run fail-safe:run locally.",
            fixCommands: ["npm run fail-safe:run", "npm run check:ci"],
          },
        ],
  };

  const runUrl = process.env.RUN_URL || "";
  const actionable = getActionableIssues(report.issues);
  const agentPrompt = formatAgentFixPrompt(log, {
    branch: process.env.HEAD_BRANCH || "",
    prUrl: process.env.PR_URL || "",
  });

  const lines = [
    "## Fail-safe bot — CI failure diagnosis",
    "",
    runUrl ? `Workflow run: ${runUrl}` : "",
    "",
    formatReportSummary(report),
    "",
    "### What happens next",
    "- **Safe auto-fix** may run on this PR (rust locks / eslint `--fix` / catalog import) and push a commit.",
    "- Comment **`@fail-safe fix`** anytime to re-run safe auto-fix on this branch.",
    "- For Sourcery / logic issues: paste into Cursor Agent, or enable the fail-safe Cursor Automation.",
    "",
    "### Agent prompt",
    "",
    "```",
    agentPrompt.trim(),
    "```",
    "",
    actionable?.length
      ? `Actionable issues: ${actionable.map((i) => i.id || i.title).join(", ")}`
      : "",
    "",
    "---",
    "_Auto-generated by `.github/workflows/fail-safe-bot.yml`._",
  ].filter((l) => l !== undefined && l !== null);

  process.stdout.write(lines.filter((l, i, a) => !(l === "" && a[i - 1] === "")).join("\n"));
}

main().catch((err) => {
  process.stderr.write(String(err?.message || err));
  process.exit(1);
});
