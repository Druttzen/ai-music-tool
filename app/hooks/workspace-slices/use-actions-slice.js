export function useWorkspaceActionsSlice(source) {
  return useMemo(
    () => buildProjectWorkspaceActions(pick(source, ACTION_KEYS)),
    [source],
  );
}

function pick(source, keys) {
  const out = {};
  for (const k of keys) out[k] = source[k];
  return out;
}
